public class LabelInfo {
    
    /**
     * 标签名称
     */
    public String name;
    /**
     * 标签颜色
     */
    public String color;
    
    @Override
    public String toString() {
        return "LabelInfo{" + "name='" + name + '\'' + ", color='" + color + '\'' + '}';
    }
}
